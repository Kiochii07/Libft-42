#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*pdest;
	unsigned char	*psrc;
	size_t			i;

	pdest = (unsigned char *)dest;
	psrc = (unsigned char *)src;
	i = 0;
	if (!pdest && !psrc)
		return (0);
	while (i < n)
	{
		pdest[i] = psrc[i];
		i++;
	}
	return ((void *)pdest);
}
